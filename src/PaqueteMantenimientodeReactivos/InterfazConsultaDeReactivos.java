    private void buscarGrado(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_buscarGrado
        // TODO add your handling code here:
        ControlGrupo Cgrupo = new ControlGrupo();
        try {
            ResultSet obtenerGrados = Cgrupo.obtenerGrupos();

            String grado = "";

            while (obtenerGrados.next()) {
                grado = obtenerGrados.getObject(1).toString();
                int totalGrado = cbox2Grado.getItemCount();
                int maximo = 0;

                for (int i = 0; i < totalGrado; i++) {
                    if (grado.compareTo(cbox2Grado.getItemAt(i).toString()) == 0) {
                        maximo++;

                    }
                }
                if (maximo == 0) {
                    this.cbox2Grado.addItem(grado);
                }

            }

        } catch (ClassNotFoundException | SQLException ex) {
            System.out.println("Error al intentar obtener datos a la bd");
        }

    }//GEN-LAST:event_buscarGrado

    //[8]muestra los comentario segun el reactivo seleccionado
    public void llenarComentarios(ResultSet conjuntoResultados[]) throws SQLException {
        int posicion = 0;
        DefaultTableModel modelo = (DefaultTableModel) table2Comentarios.getModel();

        while (conjuntoResultados[1].next()) {
            modelo.addRow(new Object[]{
                "",
                conjuntoResultados[1].getObject(1).toString(),
                "",
                ""
            });

            posicion++;
        }

        posicion = 0;
        while (conjuntoResultados[0].next()) {
            table2Comentarios.setValueAt((posicion + 1), posicion, 0);
            table2Comentarios.setValueAt(conjuntoResultados[0].getObject(1).toString(), posicion, 2);
            table2Comentarios.setValueAt(conjuntoResultados[0].getObject(2).toString(), posicion, 3);

            posicion++;
        }

        contarTotalComentario = posicion;
        lab6TotalComentarios.setText("Total de comentarios: #" + contarTotalComentario);

    }

    //[6] muestra los reactivos encontrados
    public void llenarReactivos(ResultSet conjuntoResultados) throws ClassNotFoundException, SQLException {
        table1Reactivos.removeAll();

        DefaultTableModel modelo = (DefaultTableModel) table1Reactivos.getModel();

        int posicion = 0;
        while (conjuntoResultados.next()) {
            modelo.addRow(new Object[]{
                (posicion + 1),
                conjuntoResultados.getObject(1).toString(),
                conjuntoResultados.getObject(2).toString(),
                conjuntoResultados.getObject(3).toString(),
                conjuntoResultados.getObject(4).toString(),});

            posicion++;

        }//find el while
        contarTotalReactivos = posicion;
        lab1TotaldeReactivos.setText("Total de reactivos: #" + contarTotalReactivos);

    }

