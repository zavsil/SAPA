    //[10] permite ingresar un comentario a un reactivo en especifico
    private void comentar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comentar
        limpiarComentarios();
        this.buscarComentarios(null);
        if (txta1Comentario.getText().length() > 5 && txta1Comentario.getText().length() <= 150) {
            ControlComentario comentario = new ControlComentario();
            try {
                comentario.registrar(txta1Comentario.getText(), table1Reactivos.getValueAt(table1Reactivos.getSelectedRow(), 1).toString(), idUsuario);
                JOptionPane.showMessageDialog(rootPane, "COMENTARIO REGISTRADO", "FELICIDADES", JOptionPane.WARNING_MESSAGE);

            } catch (SQLException | ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(rootPane, "SELECCIONE UN REACTIVO", "ERROR", JOptionPane.WARNING_MESSAGE);
            } catch (ArrayIndexOutOfBoundsException e) {
                JOptionPane.showMessageDialog(rootPane, "SELECCIONE UN REACTIVO", "ERROR", JOptionPane.WARNING_MESSAGE);
            }
            txta1Comentario.setText("");
            MouseEvent MouseEvent = null;
            buscarComentarios(MouseEvent);
            lab4Caracteres.setText("Caracteres: 0");
        } else {
            String mensaje = "";
            if (txta1Comentario.getText().length() < 5) {
                mensaje = "Comentario demaciado corto";
            } else if (txta1Comentario.getText().length() > 150) {
                mensaje = "Comentario demaciado largo";
            }
            JOptionPane.showMessageDialog(rootPane, "" + mensaje, "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_comentar

