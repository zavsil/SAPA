    //[12] verifica si existen espacios en blanco en el texto
    public boolean hayEspaciosEnBlanco(String texto) {
        boolean resultado = false;

        for (int i = 0; i < texto.length(); i++) {

            if ((int) texto.charAt(i) == 32) {
                resultado = true;
            } else {
                return false;
            }

        }

        return resultado;
    }

