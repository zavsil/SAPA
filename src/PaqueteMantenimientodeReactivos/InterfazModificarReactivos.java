    //[11] guarda lo modificado en la base de datos
    private void guardar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardar
        // TODO add your handling code here:
        if (materiaSeleccionada && gradoSeleccionado && (txta1Reactivo.getText().length() >= 5)
                && (txt1RespuestaCorrecta.getText().length() > 0)
                && (txt3RespuestaIncorrecta1.getText().length() > 0)
                && (txt2RespuestaIncorrecta2.getText().length() > 0)
                && hayEspaciosEnBlanco(txta1Reactivo.getText()) == false
                && hayEspaciosEnBlanco(txt1RespuestaCorrecta.getText()) == false
                && hayEspaciosEnBlanco(txt3RespuestaIncorrecta1.getText()) == false
                && hayEspaciosEnBlanco(txt2RespuestaIncorrecta2.getText()) == false) {
            if ((txta1Reactivo.getText().length() <= 150)) {

                ControlReactivo reactivoC = new ControlReactivo();

                try {
                    int resultadoConfirmacion = JOptionPane.showConfirmDialog(rootPane, "TODO ESTA CORRECTO?", "ALERTA", JOptionPane.OK_CANCEL_OPTION);
                    if (resultadoConfirmacion == 0) {

                        //valida que el usuario haya creado el reactivo
                        int resultadoIdUsuario = reactivoC.obtenerCreadordelReactivo(
                                table1Reactivos.getValueAt(table1Reactivos.getSelectedRow(), 1).toString(),
                                table1Reactivos.getValueAt(table1Reactivos.getSelectedRow(), 2).toString(),
                                table1Reactivos.getValueAt(table1Reactivos.getSelectedRow(), 3).toString(),
                                table1Reactivos.getValueAt(table1Reactivos.getSelectedRow(), 4).toString());

                        if (resultadoIdUsuario == idUsuario) {
                            boolean existe = reactivoC.existeReactivo(txta1Reactivo.getText(), txt1RespuestaCorrecta.getText(), txt3RespuestaIncorrecta1.getText(), txt2RespuestaIncorrecta2.getText());
                            if (existe == false) {

                                reactivoC.modificar(idUsuario,
                                        table1Reactivos.getValueAt(table1Reactivos.getSelectedRow(), 1).toString(),
                                        table1Reactivos.getValueAt(table1Reactivos.getSelectedRow(), 2).toString(),
                                        table1Reactivos.getValueAt(table1Reactivos.getSelectedRow(), 3).toString(),
                                        table1Reactivos.getValueAt(table1Reactivos.getSelectedRow(), 4).toString(),
                                        txta1Reactivo.getText(),
                                        txt1RespuestaCorrecta.getText(),
                                        txt3RespuestaIncorrecta1.getText(),
                                        txt2RespuestaIncorrecta2.getText());

                                txta1Reactivo.setText("");
                                txt1RespuestaCorrecta.setText("");
                                txt3RespuestaIncorrecta1.setText("");
                                txt2RespuestaIncorrecta2.setText("");

                                ActionEvent evento = null;
                                consultar(evento);
                                JOptionPane.showMessageDialog(rootPane, "REACTIVO MODIFICADO CON EXITO", "FELICIDADES", JOptionPane.WARNING_MESSAGE);

                            } else {
                                JOptionPane.showMessageDialog(rootPane, "EL REACTIVO YA EXISTE", "ERROR", JOptionPane.ERROR_MESSAGE);
                            }

                        } else {
                            JOptionPane.showMessageDialog(rootPane, "SOLO EL CREADOR DEL REACTIVO PUEDE MODIFICARLO", "ERROR", JOptionPane.ERROR_MESSAGE);
                        }

                    }

                } catch (SQLException | ClassNotFoundException ex) {
                }

            } else {
                JOptionPane.showMessageDialog(rootPane, "EL REACTIVO TIENE MAS DE 150 CARACTERES", "ERROR", JOptionPane.ERROR_MESSAGE);
            }

        } else {
            String mensaje = "";
            mensaje = datosPorIngresar();
            JOptionPane.showMessageDialog(rootPane, "NO DEJES CAMPOS VACIOS\n INGRESE LOS SIGUIENTES CAMPOS:\n" + mensaje + "", "ERROR", JOptionPane.ERROR_MESSAGE);

        }


    }//GEN-LAST:event_guardar

